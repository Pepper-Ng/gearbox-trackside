/// <summary>
/// One hash-covered file in a Trackside bundle.
/// </summary>
/// <param name="Path">Forward-slash path relative to the bundle root.</param>
/// <param name="Sha256">Lowercase SHA-256 hex digest.</param>
/// <param name="Bytes">File length in bytes.</param>
public sealed record BundleManifestFile(string Path, string Sha256, long Bytes);

/// <summary>
/// Result of checking a bundle root against a manifest.
/// </summary>
/// <param name="Success">True when all files match.</param>
/// <param name="Errors">Verification errors, if any.</param>
public sealed record BundleVerificationResult(bool Success, IReadOnlyList<string> Errors);

/// <summary>
/// Verifies manifest files against a bundle directory.
/// </summary>
public static class BundleManifestVerifier
{
    /// <summary>
    /// Verifies listed files exist, have the expected length, and match expected SHA-256 hashes.
    /// </summary>
    /// <param name="manifest">Manifest to verify.</param>
    /// <param name="bundleRoot">Bundle root containing the listed files.</param>
    /// <returns>Verification result.</returns>
    public static BundleVerificationResult Verify(BundleManifest manifest, string bundleRoot)
    {
        var errors = new List<string>();
        foreach (var manifestFile in manifest.Files)
        {
            var filePath = System.IO.Path.Combine(bundleRoot, manifestFile.Path.Replace('/', System.IO.Path.DirectorySeparatorChar));
            if (!File.Exists(filePath))
            {
                errors.Add($"Missing file: {manifestFile.Path}");
                continue;
            }

            var fileInfo = new FileInfo(filePath);
            if (fileInfo.Length != manifestFile.Bytes)
            {
                errors.Add($"Size mismatch for {manifestFile.Path}: expected {manifestFile.Bytes}, actual {fileInfo.Length}.");
            }

            using var stream = File.OpenRead(filePath);
            var actualHash = Convert.ToHexString(SHA256.HashData(stream)).ToLowerInvariant();
            if (!string.Equals(actualHash, manifestFile.Sha256, StringComparison.OrdinalIgnoreCase))
            {
                errors.Add($"SHA-256 mismatch for {manifestFile.Path}: expected {manifestFile.Sha256}, actual {actualHash}.");
            }
        }

        return new BundleVerificationResult(errors.Count == 0, errors);
    }
}

/// <summary>
/// Comparison output for a future staff-approved update flow.
/// </summary>
public sealed record UpdatePlan
{
    /// <summary>
    /// Currently installed bundle version.
    /// </summary>
    public string CurrentBundleVersion { get; init; } = string.Empty;

    /// <summary>
    /// Candidate bundle version.
    /// </summary>
    public string CandidateBundleVersion { get; init; } = string.Empty;

    /// <summary>
    /// Planned action.
    /// </summary>
    public string Action { get; init; } = "NoChange";

    /// <summary>
    /// Creates an update plan from two manifests.
    /// </summary>
    /// <param name="current">Current manifest.</param>
    /// <param name="candidate">Candidate manifest.</param>
    /// <returns>Update plan.</returns>
    public static UpdatePlan Create(BundleManifest current, BundleManifest candidate)
    {
        var action = string.Equals(current.BundleVersion, candidate.BundleVersion, StringComparison.OrdinalIgnoreCase)
            ? "NoChange"
            : "UpdateAvailable";

        return new UpdatePlan
        {
            CurrentBundleVersion = current.BundleVersion,
            CandidateBundleVersion = candidate.BundleVersion,
            Action = action,
        };
    }
}